import numpy as np

#Range 1 to 10
arr1=np.arange(10)

#Range 10 to 20
arr3=np.arange(10,20)
#Range 10 to 20 interval of 2
arr4=np.arange(10,20,2)
#One dimensional array
arr5 = np.ones(10)

#2D array
arr6=np.ones((5,4)) * 10

#Type of Dimesnion
arr7=np.ones((5,4),dtype ='int')
print(arr1)
print(arr3)
print(arr4)
print(arr5)
print(arr6)
print(arr7)
